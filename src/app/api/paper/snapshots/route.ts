import { NextRequest, NextResponse } from 'next/server'
import { auth } from '@/lib/auth'
import { prisma } from '@/lib/prisma'

export async function GET(req: NextRequest) {
  const session = await auth()
  if (!session?.user?.id) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })

  const projectId = req.nextUrl.searchParams.get('projectId')
  if (!projectId) return NextResponse.json({ error: 'projectId required' }, { status: 400 })

  const project = await prisma.researchProject.findFirst({
    where: { id: projectId, userId: session.user.id },
  })
  if (!project) return NextResponse.json({ error: 'Not found' }, { status: 404 })

  const snapshots = await prisma.documentSnapshot.findMany({
    where: { documentId: projectId },
    orderBy: { createdAt: 'desc' },
    select: { id: true, label: true, createdAt: true, content: true },
    take: 50,
  })

  return NextResponse.json(snapshots)
}

export async function POST(req: NextRequest) {
  const session = await auth()
  if (!session?.user?.id) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })

  const { projectId, content, label } = await req.json()

  const project = await prisma.researchProject.findFirst({
    where: { id: projectId, userId: session.user.id },
  })
  if (!project) return NextResponse.json({ error: 'Not found' }, { status: 404 })

  const snapshot = await prisma.documentSnapshot.create({
    data: { documentId: projectId, content, label: label ?? new Date().toISOString() },
  })

  return NextResponse.json(snapshot, { status: 201 })
}
